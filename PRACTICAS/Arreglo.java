public class Arreglo {
    public static void main(String[] args) {
        // a. Crear el arreglo con los números indicados
        int[] numeros = {12, 10, 4, 24, 5, 9};

        // b. Recorrer el arreglo e imprimir el mensaje en cada posición
        for (int i = 0; i < numeros.length; i++) {
            System.out.println("El arreglo en la posición " + i + " tiene el número " + numeros[i]);
        }
    }
}
